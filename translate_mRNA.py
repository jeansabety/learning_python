# Make a program that translates mRNA into protein
# It should report the longest ORF in each mRNA
# Make sure you translate all 3 reading frames on both strands
# It should use argparse and your library, of course
#report as a fasta 

#1. make reverse compl 
#2. look for orfs and keep track of longest one 
#3. translate the longest orf 

import mcb185 
import argparse 

parser = argparse.ArgumentParser(description='mRNA to protein')
# required arguments
parser.add_argument('--fasta', required=True, type=str, 
	metavar='<str>', help='required fasta file')
arg = parser.parse_args() 

#------initial strand------# 
for name, seq in mcb185.read_fasta(arg.fasta): 
	max_len = 0 
	max_seq = None 
	for orf in mcb185.orfseq(seq): #because of the way the formula is constructed it orfseq will return one sequence at a time rather than a list 
		if len(orf) > max_len : #if current orf is larger than the previous max length of the orfs 
			max_seq = orf 
			max_len = len(orf)
	for orf in mcb185.orfseq(mcb185.rc(seq)): #same deal but for reverse compliment 
		if len(orf) > max_len: 
			max_seq = orf 
			max_len = len(orf)
	print(f'>{name}') 
	for i in range(0, len(max_seq), 80): #this makes it 80 column 
		print(max_seq[i:i+80])



		
